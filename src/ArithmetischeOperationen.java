public class ArithmetischeOperationen {
    public static void main(String[] args) {
        int a = 22;
        int b = 24;

        float f = a/b;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(f);
        System.out.println(a%b);

    }
}
